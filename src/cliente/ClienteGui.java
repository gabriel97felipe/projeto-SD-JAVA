    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        usuarioDTO.setUsuario(login.getText());
        usuarioDTO.setLogin(login.getText());
        usuarioDTO.setSenha(senha.getText());
        try {
            usuarioDTO.setIp(InetAddress.getLocalHost().getHostAddress());
            usuarioDTO.setPorta(Integer.toString(cliente.getLocalPort()));
            System.out.println(cliente.getLocalPort());
        } catch (UnknownHostException ex) {
            Logger.getLogger(ClienteGui.class.getName()).log(Level.SEVERE, null, ex);
        }
        usuarioDTO.setOperacao(1);
        try {
            out.writeUTF(gson.toJson(usuarioDTO));
            System.out.println("Cliente enviou: " + gson.toJson(usuarioDTO));
            String msg = in.readUTF();
            System.out.println("Cliente recebeu: " + msg);
            MensagemDTO mensagemDTO = gson.fromJson(msg, MensagemDTO.class);
            if (mensagemDTO.getStatus()) {
                JOptionPane.showMessageDialog(null, mensagemDTO.getMensagem() + " - " + mensagemDTO.getData());
                login.setEditable(false);
                senha.setEditable(false);
                jButton2.setVisible(false);
                jButton1.setVisible(true);
                btnChat.setVisible(true);
                jTextField1.setEditable(true);
                jTextField2.setEditable(true);
                jTextField3.setEditable(true);
                jTextField4.setEditable(true);
                jTextField5.setEditable(true);
                jTextField6.setEditable(true);
                jTextField7.setEditable(true);
                jTextField8.setEditable(true);
                jTextField9.setEditable(true);
                jTextField10.setEditable(true);
                jTextField11.setEditable(true);
            } else {
                JOptionPane.showMessageDialog(null, mensagemDTO.getMensagem());

            }

        } catch (IOException ex) {
            Logger.getLogger(ClienteGui.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

