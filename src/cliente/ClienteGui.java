    private void btnChatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChatActionPerformed
        if (jTextField11.getText().isEmpty() || equipamentos.size() == 0) {
            JOptionPane.showMessageDialog(null, "Insira os valores!");
        } else {
            CalculoDTO calculoDTO = new CalculoDTO();
            calculoDTO.setEquipamentos(equipamentos);
            calculoDTO.setQtdPessoas(Integer.parseInt(jTextField11.getText()));
            calculoDTO.setOperacao(3);
            try {
                out.writeUTF(gson.toJson(calculoDTO));
                System.out.println("Cliente enviou: " + gson.toJson(calculoDTO));
                equipamentos.clear();
                String msg = in.readUTF();
                System.out.println("Cliente recebeu: " + msg);
                CalculoRetornoDTO crDTO = gson.fromJson(msg, CalculoRetornoDTO.class);
                if (crDTO.isStatus()) {
                    for (int i = 0; i < crDTO.getMsgs().size(); i++) {
                        JOptionPane.showMessageDialog(null, crDTO.getMsgs().get(i));
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Algo deu errado.");
                }

            } catch (IOException ex) {
                Logger.getLogger(ClienteGui.class.getName()).log(Level.SEVERE, null, ex);
            }


        }    }//GEN-LAST:event_btnChatActionPerformed

